namespace Creational\ObjectPool;

class SomeObject implements ObjectInterface
{
    /**
     * @var string
     */
    private $name;

    /**
     * City constructor.
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
